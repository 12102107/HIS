package com.znyy.bean;
 /** 
 * @类名称: SmSystemConfig
 * @类描述:系统及参数配置表
 * @作者:付士山
 * @创建时间:2016-1-12 下午1:17:12
 */
 
public class SmSystemConfig implements java.io.Serializable {
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String category;
	private String code;
	private String name;
	private String value;
	private String enable;

	// Constructors

	/** default constructor */
	public SmSystemConfig() {
	}

	/** full constructor */
	public SmSystemConfig(String category, String code, String name,
			String value, String enable) {
		this.category = category;
		this.code = code;
		this.name = name;
		this.value = value;
		this.enable = enable;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getCategory() {
		return this.category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getCode() {
		return this.code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return this.value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getEnable() {
		return this.enable;
	}

	public void setEnable(String enable) {
		this.enable = enable;
	}

}